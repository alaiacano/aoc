package com.github.alaiacano.aoc2020

import collection.mutable.{Map => MMap}

object Dec3 extends App {
  def parseInputMapping(dots: String): (Int, Map[Int, Set[Int]]) = {
    val mapping = MMap.empty[Int, Set[Int]]
    dots.split("\n").zipWithIndex.foreach {
      case (rowString, rowIndex) => {
        // We need to initialize in case the last row has no trees
        mapping(rowIndex) = Set.empty[Int]
        rowString.zipWithIndex.foreach {
          case ('#', i) => mapping(rowIndex) = mapping(rowIndex) ++ Set(i)
          case _        => {}
        }
      }
    }
    (dots.split("\n").head.length, mapping.toMap)
  }

  def ski(trees: String, rightMove: Int, downMove: Int): Int = {
    val (gridWidth, treeMap) = parseInputMapping(trees)
    var (x, numTrees) = (0, 0)
    val numRows: Int = treeMap.keys.max

    (0 to numRows by downMove).foreach(row => {
      if (treeMap(row).contains(x)) {
        numTrees += 1
      }
      x = (x + rightMove) % gridWidth
    })

    numTrees
  }
  println(s"DEMO: ${ski(Dec3Input.demoInput, 3, 1)}")
  println(s"ACTUAL: ${ski(Dec3Input.input, 3, 1)}")

  println(s"Right 1, down 1. ${ski(Dec3Input.input, 1, 1)}")
  println(s"Right 3, down 1. ${ski(Dec3Input.input, 3, 1)}")
  println(s"Right 5, down 1. ${ski(Dec3Input.input, 5, 1)}")
  println(s"Right 7, down 1. ${ski(Dec3Input.input, 7, 1)}")
  println(s"Right 1, down 2. ${ski(Dec3Input.input, 1, 2)}")

  val finalProduct: Long = List((1, 1), (3, 1), (5, 1), (7, 1), (1, 2))
    .map { case (right, down) => ski(Dec3Input.input, right, down).toLong }
    .foldLeft(1L)(_ * _)
  println(s"Product of all combinations: $finalProduct")

}

